    private void layDsNv(String search) {

        listRole = roleBUS.layDsVaiTro();
        cbbVaitro.removeAllItems();
        roleMap.clear();

        for (RoleDTO vt : listRole) {
            cbbVaitro.addItem(vt.getNamerole());
            roleMap.put(vt.getNamerole(), vt.getIdrole());
        }
        btnAdd.setEnabled(true);
        nvBUS.layDsNv(tblNv, search);
    }

