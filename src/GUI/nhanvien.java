    private void clickChooseTblNv() {

        row = tblNv.getSelectedRow();
        if (row != -1) {
            btnAdd.setEnabled(false);
            txtIdnv.setText(tblNv.getValueAt(row, 0) + "");
            txtNamenv.setText(tblNv.getValueAt(row, 1) + "");
            txtEmail.setText(tblNv.getValueAt(row, 2) + "");
            txtPhone.setText(tblNv.getValueAt(row, 3) + "");
            txtAddress.setText(tblNv.getValueAt(row, 4) + "");
            if ((tblNv.getValueAt(row, 5) + "").equals("Nam")) {
                cbbSex.setSelectedIndex(0);
            } else {
                cbbSex.setSelectedIndex(1);
            }
            cbbVaitro.setSelectedItem(tblNv.getValueAt(row, 7) + "");
            Object ngaySinhObj = tblNv.getValueAt(row, 6);
            if (ngaySinhObj instanceof Date) {
                // Nếu là java.sql.Date, gán trực tiếp
                csBirthday.setDate((java.sql.Date) ngaySinhObj);
            }
        }
    }

